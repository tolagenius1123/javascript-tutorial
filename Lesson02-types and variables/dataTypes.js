// Data types represent the different kinds of values we can use
// in JavaScript.
// There are altogether 8 basic data types in JavaScript
// JavaScript data types are divided into primitive and non-primitive types
// Primitive data types can hold a simple value e.g string, number, boolean
// Non Primitive data types can hold multiple values e.g objects

// String
let firstName = "Omotola";
let lastName = "Jinadu";
let fullname = `${firstName} ${lastName}`;

// Number
let yearOfBirth = 2030;

// BigInt
let result = 900719925124740999n;

// Boolean
let isLoading = false;

// Undefined
let num;
console.log(num);

// Null
let age = null;

// Symbol
let value = Symbol("Hello World");

// Object
let student = {
	name: "John Wick",
	department: "English",
};
